library(ggplot2)
library(reshape2)
library(RColorBrewer)
args<-commandArgs(T)
data_P<-read.table(args[3],sep="\t",header=T,row.names=1)
data_N<-read.table(args[4],sep="\t",header=T,row.names=1)
N<-melt(data_N)
P<-melt(data_P)
getPalette = colorRampPalette(brewer.pal(9, "Set1"))
pdf(args[1])
ggplot()+geom_jitter(aes(x=N[,1],y=N[,2]),colour="#67a9cf",size=0.1)+geom_jitter(aes(x=P[,1],y=P[,2]),colour="#ef8a62",size=0.1)+ylab("Events percent")+xlab("Zscore")+theme(axis.text.x = element_text(angle=60, hjust=1, vjust=1),panel.grid.major=element_blank(),panel.grid.minor=element_blank())+ scale_fill_manual(values = getPalette(22))+theme_bw()+theme(panel.grid.major=element_blank(),panel.grid.minor=element_blank())
dev.off()
