library(reshape2)
options(digits=15)
args<-commandArgs(T)
#args1 is the D Nsmix input args2 is the positive input
#args3 is the I Nsmix input args4 is the positive input
data<-read.table(args[1],sep="\t",header=T,row.names=1,check.names=F)
data_N<-melt(data)
data<-read.table(args[2],sep="\t",header=T,row.names=1,check.names=F)
data_P<-melt(data)
P<-cbind(data_P,rep("Positive",length(data_P[,1])))
N<-cbind(data_N,rep("Negative",length(data_N[,1])))
colnames(P)<-c("Zscore","Percent","label")
colnames(N)<-c("Zscore","Percent","label")
Input<-rbind(P,N)
temp<-paste(args[5],"SVMInput_D",sep ="_")
write.table(Input,temp,sep="\t",quote=F,row.name=F,col.names=T)
data<-read.table(args[3],sep="\t",header=T,row.names=1,check.names=F)
data_N<-melt(data)
data<-read.table(args[4],sep="\t",header=T,row.names=1,check.names=F)
data_P<-melt(data)
P<-cbind(data_P,rep("Positive",length(data_P[,1])))
N<-cbind(data_N,rep("Negative",length(data_N[,1])))
colnames(P)<-c("Zscore","Percent","label")
colnames(N)<-c("Zscore","Percent","label")
Input<-rbind(P,N)
temp<-paste(args[5],"SVMInput_I",sep ="_")
write.table(Input,temp,sep="\t",quote=F,row.name=F,col.names=T)
